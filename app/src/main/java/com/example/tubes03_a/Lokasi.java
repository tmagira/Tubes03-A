package com.example.tubes03_a;

public class Lokasi {
    private String lokasi;
    public Lokasi(){}

    //Constructor
    public Lokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getLokasi() {
        return lokasi;
    }
}
